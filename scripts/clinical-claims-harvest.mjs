// Bulk Category-2 clinical-claims harvest (P-C). Enumerates the hard-coded
// clinical constants and citations across the assessment runners into a
// machine-generated register annex, so Tranche-2 (formulas/normatives) and
// Tranche-3 (citation integrity) can be worked from a concrete inventory rather
// than by hand. Read-only over the repo.
// Run: node scripts/clinical-claims-harvest.mjs
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.join(__dirname, '..');
const dir = path.join(repoRoot, 'src', 'components', 'assessments');

const PATTERNS = {
  doi: /10\.\d{4,9}\/[^\s"'`)\]]+/g,
  pmid: /PMID:?\s*\d+/gi,
  normTable: /\b([A-Z][A-Z0-9_]*_NORMS|NORMATIVE_DATA|maleNorms|femaleNorms|FAT_NORMS|VO2_NORMS)\b/g,
  cutoff: /\b(severity|cutoff|threshold|classif|getInterpretation)\b/gi,
  metVo2: /\b(VO2\s*max|VO2max|\bMET\b|metabolic equivalent|\* ?3\.5|\/ ?3\.5|body density|Siri|Jackson[- ]?Pollock)\b/gi,
  diagStat: /\b(sensitivity|specificity|likelihood ratio|\bLR[+-]|MCID|MDC|ICC)\b/gi,
};

const files = fs.readdirSync(dir).filter((f) => /Runner.*\.jsx$/.test(f) || /\.jsx$/.test(f) && /Runner/.test(f));
const rows = [];
const doiInventory = [];
let totals = { files: 0, withMarkers: 0, doi: 0, pmid: 0, normTable: 0, cutoff: 0, metVo2: 0, diagStat: 0 };

for (const f of files) {
  const full = path.join(dir, f);
  let text;
  try { text = fs.readFileSync(full, 'utf8'); } catch { continue; }
  totals.files += 1;
  const counts = {};
  for (const [k, re] of Object.entries(PATTERNS)) {
    const m = text.match(re) || [];
    // DOIs are counted by unique value per file so the total agrees with the
    // deduplicated inventory below; other markers count raw occurrences.
    counts[k] = k === 'doi' ? new Set(m.map((d) => d.replace(/[.,;)\]]+$/, '').toLowerCase())).size : m.length;
    totals[k] += counts[k];
  }
  // Collect unique DOIs with a line reference.
  const doiSet = new Set();
  text.split('\n').forEach((line, i) => {
    const found = line.match(PATTERNS.doi);
    if (found) found.forEach((d) => {
      const clean = d.replace(/[.,;)\]]+$/, '');
      if (!doiSet.has(clean)) { doiSet.add(clean); doiInventory.push({ file: f, line: i + 1, doi: clean }); }
    });
  });
  const total = Object.values(counts).reduce((a, b) => a + b, 0);
  if (total > 0) totals.withMarkers += 1;
  rows.push({ file: f, total, ...counts });
}

rows.sort((a, b) => b.total - a.total);

const lines = [];
lines.push('# Clinical-Claims Harvest — Category 2 Inventory');
lines.push('');
lines.push('Machine-generated by `scripts/clinical-claims-harvest.mjs` (re-run to refresh). Annex to `20260705-clinical-claims-audit-register.md`. This inventories hard-coded clinical constants and citations across the assessment runners so Tranche 2 (formulas/normatives) and Tranche 3 (citation integrity) can be verified from a concrete list.');
lines.push('');
lines.push('## Totals');
lines.push('');
lines.push(`- Runner files scanned: **${totals.files}**; with at least one clinical-claim marker: **${totals.withMarkers}**`);
lines.push(`- Hard DOI literals: **${totals.doi}** (unique inventory below); PMID markers: **${totals.pmid}**`);
lines.push(`- Named normative tables: **${totals.normTable}**; severity/cutoff/classify markers: **${totals.cutoff}**`);
lines.push(`- MET/VO2/body-composition formula markers: **${totals.metVo2}**; diagnostic-stat markers: **${totals.diagStat}**`);
lines.push('');
lines.push('## Top 30 constant-bearing runners (by marker count)');
lines.push('');
lines.push('| File | Total | DOI | PMID | NormTbl | Cutoff | MET/VO2 | DiagStat |');
lines.push('|---|--:|--:|--:|--:|--:|--:|--:|');
for (const r of rows.slice(0, 30)) {
  lines.push(`| ${r.file} | ${r.total} | ${r.doi} | ${r.pmid} | ${r.normTable} | ${r.cutoff} | ${r.metVo2} | ${r.diagStat} |`);
}
lines.push('');
lines.push(`## DOI inventory for Tranche-3 verification (${doiInventory.length} unique literals)`);
lines.push('');
lines.push('Each of these must be resolved AND support-checked via the `verifyReferences` service before it may be presented as authoritative.');
lines.push('');
lines.push('| File | Line | DOI |');
lines.push('|---|--:|---|');
for (const d of doiInventory) {
  lines.push(`| ${d.file} | ${d.line} | ${d.doi} |`);
}
lines.push('');

const outPath = path.join(repoRoot, 'docs', 'qa', '20260706-clinical-claims-harvest.md');
fs.writeFileSync(outPath, lines.join('\n'), 'utf8');
console.log(`Harvest complete: ${totals.files} runners scanned, ${totals.withMarkers} with markers, ${doiInventory.length} unique DOIs.`);
console.log(`Wrote ${path.relative(repoRoot, outPath)}`);
